export const getTranslation = (key, language = 'en') => {
  const keys = key.split('.');
  let value = translations[language] || translations['en'];
  
  for (const k of keys) {
    value = value?.[k];
    if (value === undefined) {
      return translations['en'][key] || key;
    }
  }
  
  return value || translations['en'][key] || key;
};
